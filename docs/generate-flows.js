// Read all .md files and build FILE_CONTENTS object
function readMdFiles() {
  const entries = [];
  for (const rel of MD_FILES) {
    const abs = path.join(ROOT, rel);
    if (!fs.existsSync(abs)) {
      console.warn(`  skip (not found): ${rel}`);
      continue;
    }
    const content = fs.readFileSync(abs, 'utf-8');
    // JSON.stringify handles all escaping
    entries.push(`${JSON.stringify(rel)}: ${JSON.stringify(content)}`);
  }
  return `const FILE_CONTENTS = {\n${entries.join(',\n')}\n};`;
}

